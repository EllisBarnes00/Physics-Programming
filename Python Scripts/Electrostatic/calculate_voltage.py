# Voltage is the electric potential

# Electric Potential Energy: Energy possessed by a charged particle due to its 
# location in an electric field. Work is required to push a 
# charged particle against the electric field of a charged 
# body.


electric_potential_energy = float(input("Electric Potential Energy: "))
amount_of_charge = int(input("Charge amount: "))

voltage = electric_potential_energy/amount_of_charge

print("Voltage/Electric Potential: {0} V".format(voltage))
